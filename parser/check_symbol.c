#include "parser.h"

int	is_tokensep(char c)
{
	if (c == '>'
		|| c == '<'
		|| c == '|'
		|| c == ';')
		return (1);
	return (0);
}

int	is_quotes(char c)
{
	if (c == '\''
		|| c == '\"')
		return (1);
	return (0);
}
