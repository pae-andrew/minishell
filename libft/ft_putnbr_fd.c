#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	c;

	if (n / 10)
	{
		ft_putnbr_fd(n / 10, fd);
	}
	else if (n < 0)
	{
		write(fd, "-", 1);
	}
	if (n < 0)
	{
		c = '0' + (-1) * (n % 10);
	}
	else
	{
		c = '0' + (n % 10);
	}
	write(fd, &c, 1);
}
